# las más conocidas operaciones...
print(10 + 10)  # Suma o Adición
print(10 - 5)   # Resta
print(10 * 2)   # Multiplicación
print(10 / 2)   # División
# y las no tan conocidas o propias de Python...
print(10 % 3)   # Residuo
print(10 // 3)  # Cociente
print(10 ** 2)  # Exponenciación

# ejemplo de jerarquia de operadores (PEMDAS)
print(2 ** 3 + 3 - 7 / 1 // 4)
print(8 + 3 - 7 // 4)
print(8 + 3 - 1)
print(10)
