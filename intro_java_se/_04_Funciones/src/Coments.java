public class Coments {
    /**
     * Descrpción: Función que realiza la suma de dos números enteros
     *
     * @param a Primer número entero
     * @param b Segundo número entero
     * @return Devuelve la suma de los dos números enteros
     * */
    public static int suma(int a, int b){
        return a + b;
    }

    public static void main(String[] args) {
        // comentario de una sola línea

        /*
        * Comentarío de
        * multiples
        * lineas
        * */

        /**
         * Comentarios de tipo
         * JavaDoc para poder crear
         * documentación
         * */

        int a = 2;
        int b = 5;
        System.out.println(suma(a, b)); // coloca el mouse sobre el nombre de la función
    }
}
